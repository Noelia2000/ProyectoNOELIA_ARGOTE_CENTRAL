

const dia ='2';

switch(dia){
    case 0:
        console.log('Hoy es domingo');
        break;
    case 1:
        console.log('Hoy es lunes');
        break;
    case '2':
        console.log('Hoy es martes');
        break;
        default:
            console.log('No es domingo, lunes o martes');
            break;

}

